package infinite;

public class If_else_ifSatement {
	
	 public static void main(String[] args) {

		    int number = 0; // Declare an integer variable named 'number' and initialize it with the value 0.


		    // checks if number is greater than 0  and display a message if true.
		    if (number > 0) {
		      System.out.println("The number is positive.");
		    }

		    // checks if number is less than 0
		    else if (number < 0) {
		      System.out.println("The number is negative.");
		    }
		    
		    // if both condition is false
		    else {
		      System.out.println("The number is 0.");
		    }
		  }

}
